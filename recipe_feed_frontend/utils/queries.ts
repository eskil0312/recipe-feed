
export const allRecipesQuery = () => {
  const query = `*[_type == "recipe"] | order(_createdAt desc){
    _id,
     title,
       video{
        asset->{
          _id,
          url
        }
      },
      userId,
      postedBy->{
        _id,
        userName,
        image
      },
    likes,
    comments[]{
      comment,
      _key,
      postedBy->{
      _id,
      userName,
      image
    },
    }
  }`;

  return query;
};

